#!/usr/bin/env python3
"""Write a TRACKED manifest of the gitignored `motion_out/assets_src/` asset library.

`dataset/motion_out/` is gitignored (generated blobs + ~330 MB of downloaded assets), so when the
box is rebuilt the asset library has to be re-fetched. This records exactly what was staged --
name, source family, author, licence (read from each MakeHuman `.mhclo` header), file list and
sha256 -- so a new instance can re-download and VERIFY it got the same bytes.

    python make_assets_manifest.py --src motion_out/assets_src --out ASSETS_MANIFEST.md
"""
import argparse, hashlib, os, textwrap

ap = argparse.ArgumentParser()
ap.add_argument("--src", default="motion_out/assets_src")
ap.add_argument("--out", default="ASSETS_MANIFEST.md")
args = ap.parse_args()

# Where each family came from. The per-asset author/licence is read from the .mhclo headers below.
SOURCES = {
    "hair": "MakeHuman Community asset portal (http://www.makehumancommunity.org/assets.html) — "
            "user-contributed MHCLO assets, per-asset licence in the .mhclo header",
    "eyebrows": "MakeHuman Community asset portal — same as hair",
    "clothing": "MakeHuman Community asset portal — same as hair",
    "skins": "SMOKEWORKS/ZSTUDIOS itch.io skin packs — see skins/PROVENANCE.md (CC BY 4.0, attribution required)",
    "heads": "Lee Perry-Smith 'Infinite' head — see heads/.../PROVENANCE.md (CC-BY 3.0, attribution required)",
}


def sha256(path, cap=64 << 20):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(1 << 20), b""):
            h.update(chunk)
    return h.hexdigest()


def mhclo_meta(asset_dir):
    """MakeHuman .mhclo files carry `# author X` / `# license Y` comment headers."""
    for fn in sorted(os.listdir(asset_dir)):
        if not fn.endswith(".mhclo"):
            continue
        author = lic = None
        with open(os.path.join(asset_dir, fn), errors="ignore") as f:
            head = [ln for ln in f if ln.startswith("#")][:8]
        for line in head:
            low = line.lower()
            if "author" in low:
                author = line.split(None, 2)[-1].strip()
            elif "license" in low:
                lic = line.split(None, 2)[-1].strip()
        if author is None and head:
            # Some contributors use a single free-form credit line, e.g. "# Cortu Johnstone - CC0".
            txt = head[0].lstrip("# ").strip()
            for tag in ("CC0", "CC_by", "CC-BY", "CC BY"):
                if tag.lower() in txt.lower():
                    author, lic = txt.split("-")[0].strip(), tag
                    break
            else:
                author = txt or None
        return author, lic
    return None, None


rows, total_bytes, total_files = [], 0, 0
for family in sorted(os.listdir(args.src)):
    fam_dir = os.path.join(args.src, family)
    if not os.path.isdir(fam_dir):
        continue
    # clothing nests one level deeper (tops/ bottoms/)
    groups = [(family, fam_dir)]
    subs = [d for d in sorted(os.listdir(fam_dir)) if os.path.isdir(os.path.join(fam_dir, d))]
    if subs and all(os.path.isdir(os.path.join(fam_dir, d)) for d in subs) and family == "clothing":
        groups = [(f"{family}/{d}", os.path.join(fam_dir, d)) for d in subs]
    for label, gdir in groups:
        for asset in sorted(os.listdir(gdir)):
            adir = os.path.join(gdir, asset)
            if not os.path.isdir(adir):
                continue
            files = sorted(f for f in os.listdir(adir) if os.path.isfile(os.path.join(adir, f)))
            nbytes = sum(os.path.getsize(os.path.join(adir, f)) for f in files)
            author, lic = mhclo_meta(adir)
            if lic is None and os.path.exists(os.path.join(adir, "PROVENANCE.md")):
                lic = "see PROVENANCE.md"
            elif lic is None and os.path.exists(os.path.join(gdir, "PROVENANCE.md")):
                lic = "see ../PROVENANCE.md"
            rows.append((label, asset, author or "-", lic or "-", len(files), nbytes,
                         [(f, sha256(os.path.join(adir, f))[:16]) for f in files]))
            total_bytes += nbytes
            total_files += len(files)

with open(args.out, "w") as f:
    f.write("# ASSETS MANIFEST — `dataset/motion_out/assets_src/` (gitignored, must be re-fetched)\n\n")
    f.write(f"Generated by `dataset/make_assets_manifest.py`. **{len(rows)} assets, {total_files} files, "
            f"{total_bytes/1e6:.0f} MB.**\n\n")
    f.write("This directory is NOT in git (too large). To rebuild a box: either copy `assets_src/` across "
            "directly (fastest), or re-download per the source table below and verify with the sha256 "
            "prefixes.\n\n## Sources\n\n")
    for k, v in SOURCES.items():
        f.write(f"- **{k}/** — {v}\n")
    # Standing project guardrail: everything shipped must be commercial + redistributable. A
    # copyleft header on a browser-delivered asset is disqualifying (AGPL's network clause; and the
    # Unreal EULA separately names GPL/LGPL/CC-BY-SA as "Non-Compatible Licenses"). Surface them.
    risky = [r for r in rows if any(t in (r[3] or "").upper() for t in ("GPL", "SHAREALIKE", "BY-SA"))]
    if risky:
        f.write("\n## ⚠️ LICENCE RISK — do not ship these without re-verifying\n\n")
        f.write(f"{len(risky)} asset(s) carry a **copyleft** licence header. AGPL's network clause would "
                "reach a hosted avatar service, and copyleft is separately incompatible with the Unreal "
                "EULA's Non-Compatible License clause. MakeHuman's own position is that *outputs* are CC0 "
                "while the *software* is AGPL, so these headers may be boilerplate — but that has to be "
                "confirmed per asset, not assumed.\n\n")
        for label, asset, author, lic, *_ in risky:
            f.write(f"- `{label}/{asset}` — {author} — **{lic}**\n")
    f.write("\n## Assets\n\n| family | asset | author | licence | files | MB |\n|---|---|---|---|--:|--:|\n")
    for label, asset, author, lic, n, nb, _ in rows:
        f.write(f"| {label} | `{asset}` | {author} | {lic} | {n} | {nb/1e6:.1f} |\n")
    f.write("\n## File checksums (sha256, first 16 hex)\n\n")
    for label, asset, _, _, _, _, files in rows:
        f.write(f"\n**{label}/{asset}**\n\n")
        for fn, h in files:
            f.write(f"- `{h}`  {fn}\n")

print(f"[manifest] {len(rows)} assets / {total_files} files / {total_bytes/1e6:.0f} MB -> {args.out}")
